function html_for_letter ( lettercode ) {
		
		var letterlayout = `<span class="tibetan_letter audio_info" onclick="play('!!!letter_code!!!')">&#!!!letter_code!!!;<br/></span><span class=uname>!!!letter_name!!!</span><input type="image" src="memory/!!!letter_code!!!.jpg" value="&#!!!letter_code!!!;"></input>`
	
		var html = letterlayout.replaceAll("!!!letter_code!!!", "x" + ("0000" + lettercode.toString(16).toUpperCase()).substr(-4));
		html = html.replaceAll( "!!!letter_name!!!", l.get(lettercode).name);

	
		
		
		
		//var html ="&#x" + ("0000" + lettercode.toString(16).toUpperCase()).substr(-4);
		return( html);
	}
	
	
	
	
